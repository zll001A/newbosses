package com.shsxt.newbosses.po;
/**
 * 学校表
 * @author dell
 *
 */
public class New_School {
	private String schId; //学校编号  主键
	private String schName;//学校名称
	private String schLocal;//学校所处地域
	
	
	public String getSchId() {
		return schId;
	}
	public void setSchId(String schId) {
		this.schId = schId;
	}
	public String getSchName() {
		return schName;
	}
	public void setSchName(String schName) {
		this.schName = schName;
	}
	public String getSchLocal() {
		return schLocal;
	}
	public void setSchLocal(String schLocal) {
		this.schLocal = schLocal;
	}
	
	
}
